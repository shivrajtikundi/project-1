import { ToastContainer, toast } from "react-toastify";
export const alertService = {      
    throwWarning,
    throwError,
    throwSuccess
};
function throwWarning(msg){
    toast.warn(msg, {
        position: "bottom-center",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
    });
}
